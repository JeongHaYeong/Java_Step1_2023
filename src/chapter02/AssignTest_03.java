package Chapter02;

public class AssignTest_03 {

	public static void main(String[] args) {
		
		int num1=10;
		int num2=20;
		
		System.out.println(++num1); //num1=num1+1   -> ++은 +1 
		System.out.println(--num1); //num1=num1+1   -> --은 -1 

		System.out.println(+num2); //양수로 표시만 
		System.out.println(-num2); //음수로 표시만 
		System.out.println(num2);
		
	
		
	}
}
