package Chapter01;

public class TypeTest {

	public static void main(String[] args) {
		// 위 코드 불러오려면  main 타입 후 Crtl space 후 enter

		byte aVar=-128; // 8bit = 1Byte -128 ~ 127
		short bVar=30000; // 16bit = 2Byte -32768 ~ 32767 
		int cVar=50000; // 32bit = 4byte
		long dVar=1000000L; // 64bit = 8byte
		
		System.out.println(aVar);
		System.out.println(bVar);
		System.out.println(cVar);
		System.out.println(dVar);
	
	
	}
}
